public class Player
{
    private int Telor;
    private int Uang;
    private boolean isPlaying;
	
    public void setTelor(int Telor) {
        this.Telor = Telor;
    }
    public int getTelor() {
        return Telor;
    }
    public void setUang(int Uang) {
        this.Uang = Uang;
    }
    public int getUang() {
        return Uang;
    }
    public boolean Playing() {
        return isPlaying;
    }
}
